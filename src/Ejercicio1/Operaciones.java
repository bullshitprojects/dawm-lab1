
package Ejercicio1;

public class Operaciones extends Factorial{
    public double Cuadrado (double x){
        return (Math.pow(x, 2));
    }
    
    public double Cubo (double x){
        return (Math.pow(x, 3));
    }
    
    public double expY (double x, double y){
        return (Math.pow(x, y));
    }
    
    public double Seno (double x){
        return (Math.sin(x));
    }
    
    public double Coseno (double x){
        return (Math.cos(x));
    }
    
    public double Tangente (double x){
        return (Math.tan(x));
    }
}
